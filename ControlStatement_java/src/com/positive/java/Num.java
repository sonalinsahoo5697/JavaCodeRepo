package com.positive.java;
//using if_else statement check the number 45 is positive or negative 
public class Num {

	public static void main(String[] args) {
		int n=45;
		if(n>=0) {
			System.out.println(n+ " is a positive number..");
		}
		else {
			System.out.println(n+ " is not a positive number..");
		}
	}

}
