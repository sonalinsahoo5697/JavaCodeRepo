package com.triangle.java;

public class triangle {

	public static void main(String[] args) {
		int base , height;
		float area;
		base=10 ; height=15;
		area= 1/(float)2*(base*height);
		System.out.println("Area of Triangle is: "+ area);
		
		//perimeter
		int a,b,c,s; 
		a=5;b=10;c=15;
		s=(a+b+c)/2;
		System.out.println("Perimeter of Triangle is:"+s);
		
	}

}
