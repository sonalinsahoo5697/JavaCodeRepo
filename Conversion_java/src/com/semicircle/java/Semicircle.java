package com.semicircle.java;

public class Semicircle {

	public static void main(String[] args) {
		int r=5;
		double area=1/(float)2*(3.14*r*r);
		double perimeter=(3.14*r) + (2*r);
		System.out.println("Area of Semicircle is:"+area );
		System.out.println("Perimeter of Semicircle is:"+perimeter );
	}

}
