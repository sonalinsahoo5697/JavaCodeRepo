package com.swapping.java;
//swap two number  using 3rd variable
public class Swapping {

	public static void main(String[] args) {
		int a=10 , b=20, c;
        c=a; //c=10
        a=b; //a=20
        b=c; //b=10
        System.out.println("value of a is:" +a);
        System.out.println("value of b is:" +b);
	}

}
